import { NextResponse } from "next/server";
import { connectDB } from "@/lib/db";
import User from "@/models/User";
import bcrypt from "bcryptjs";

export async function GET() {
    try {
        await connectDB();
        const user = await User.findOne({ role: "admin" }).select("-password");
        return NextResponse.json(user);
    } catch (error: any) {
        return NextResponse.json({ error: error.message }, { status: 500 });
    }
}

export async function POST(request: Request) {
    try {
        await connectDB();
        const body = await request.json();
        const { _id, name, email, contact, designation, avatar, currentPassword, newPassword } = body;

        const user = await User.findById(_id);
        if (!user) {
            return NextResponse.json({ error: "User not found" }, { status: 404 });
        }

        // Handle Profile Update
        if (name) user.name = name;
        if (email) user.email = email;
        if (contact) {
            if (!/^\d{10}$/.test(contact)) {
                return NextResponse.json({ error: "Invalid phone number format (10 digits required)" }, { status: 400 });
            }
            user.contact = contact;
        }
        if (designation) user.designation = designation;
        if (avatar) user.avatar = avatar;

        // Handle Password Update
        if (currentPassword && newPassword) {
            const isMatch = await bcrypt.compare(currentPassword, user.password);
            if (!isMatch) {
                return NextResponse.json({ error: "Incorrect current password" }, { status: 400 });
            }
            user.password = await bcrypt.hash(newPassword, 10);
        }

        await user.save();

        const responseUser = user.toObject();
        delete responseUser.password;

        return NextResponse.json(responseUser);
    } catch (error: any) {
        console.error("Profile Update Error:", error);
        return NextResponse.json({ error: error.message }, { status: 500 });
    }
}

